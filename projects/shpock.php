                <h4>Project prep</h4>
                <p>Before doing any work or deciding what kind of research would give us the clearest understanding of the problem, I spent time with the project’s stakeholders to give me a better picture of the company’s strategy and which segments of the market we’ll be going after, what we tried before, what the current user base looks like and more.</p> 

                <h4>Exploratory research</h4>
                <p>To understand how Shpock users think about this issue we agreed that we’ll need a few sessions of exploratory research and my recommendation was that we’d get the best results out of <span class="bold">moderated user interviews</span> with a small sample of users combined with a larger scale <span class="bold">survey.</span></p> 
        </div>

        <div class="project-image" id="shpock-02"></div>

        <div class="narrow-container">
                <p>The survey was sent to 10k shpock users and had an almost 10% open rate and the 6 moderated sessions were carried out the same week.</p>
