                <h1>Shpock</h1>
                <p>Shpock is an online marketplace that has been around since 2012 with a focus on low-value items exchanged in person. After seeing success in the UK market, the company decided to shift towards a transactional model where higher value items from more aspirational brands are sold online.</p>

                <div class="grey-box">
                        <div>
                                <h4>The challenge</h4>
                                <p>Shpock developed the technology to support this throughout 2019, but very few people were using this new way of buying and selling, so the bulk of my work was focused on helping users understand the benefits of this new model.</p>
                        </div>
                </div>

                <div class="grey-box">
                        <div>
                                <h4>The results</h4>
                                <p>The initial changes I made to the experience resulted in a 33% increase in the number of deliverable listings, which was my squad’s OKR for Q1.</p>
                        </div>
                        <div id="result">
                                <img src="../images/increase-arrow.svg">
                                <h2>33%</h2>
                        </div>
