                <h4>First designs & validation</h4>
                <p>After validating the concepts via another round of moderated interviews, I started refining the design towards a version we could launch. We continued to validate and user test each iteration.</p>
                <p>We realised that for this feature to be successful, we'll need to introduce the concept of secure delivery and explain the benefits to all users, which we achieved via the new onboarding journey.</p>
                <p>We also needed to make sure users understand what they get and what they miss out on if they disable secure delivery, so the point where they make the choices explains this much more clearly.</p>
